//Swap two variables
console.log("---Swapping Two Variables---");
var sweety = "Sweety";
var cutie = "Cutie";
var temp;
console.log("Before Swapping:--", "sweety:", sweety,"|","cutie:", cutie);

temp = sweety;
sweety = cutie;
cutie = temp;
console.log("After Swapping:--", "sweety:", sweety,"|","cutie:", cutie);
